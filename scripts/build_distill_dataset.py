"""Merge two sam3_autolabel outputs (train + val) into the canonical
YOLO-Seg directory layout and write a dataset YAML using COCO's 80
class names.

Why 80 names: the student starts from yolov8n-seg.pt (COCO-pretrained,
80 classes). If the training dataset YAML declares 1 class, Ultralytics
resets the head — destroying the pretrained 'person' weights. By
declaring all 80 COCO names but only emitting class-0 (person/
pedestrian) labels, the head structure is preserved; the loss only
backprops through class 0, leaving the other 79 logits frozen.

Layout produced:
  out_dir/
    images/train/*.jpg   <- copies from src_train_dir/images
    images/val/*.jpg
    labels/train/*.txt   <- copies from src_train_dir/labels
    labels/val/*.txt
    dataset.yaml         <- 80 class names, single class 0 used
"""

from __future__ import annotations

import argparse
import shutil
from pathlib import Path

# COCO 2017 80-class names in canonical YOLO order.
COCO_NAMES = [
    "person", "bicycle", "car", "motorcycle", "airplane", "bus", "train",
    "truck", "boat", "traffic light", "fire hydrant", "stop sign",
    "parking meter", "bench", "bird", "cat", "dog", "horse", "sheep",
    "cow", "elephant", "bear", "zebra", "giraffe", "backpack", "umbrella",
    "handbag", "tie", "suitcase", "frisbee", "skis", "snowboard",
    "sports ball", "kite", "baseball bat", "baseball glove", "skateboard",
    "surfboard", "tennis racket", "bottle", "wine glass", "cup", "fork",
    "knife", "spoon", "bowl", "banana", "apple", "sandwich", "orange",
    "broccoli", "carrot", "hot dog", "pizza", "donut", "cake", "chair",
    "couch", "potted plant", "bed", "dining table", "toilet", "tv",
    "laptop", "mouse", "remote", "keyboard", "cell phone", "microwave",
    "oven", "toaster", "sink", "refrigerator", "book", "clock", "vase",
    "scissors", "teddy bear", "hair drier", "toothbrush",
]


def copy_split(src: Path, dst_images: Path, dst_labels: Path) -> int:
    """Copy images + labels from a sam3_autolabel output into split dirs."""
    src_images = src / "images"
    src_labels = src / "labels"
    if not src_images.exists() or not src_labels.exists():
        raise FileNotFoundError(f"Missing images/ or labels/ in {src}")

    dst_images.mkdir(parents=True, exist_ok=True)
    dst_labels.mkdir(parents=True, exist_ok=True)
    n = 0
    for label_file in src_labels.glob("*.txt"):
        # Skip empty labels (no person detected). Including empty samples
        # is fine for training but slows it down; we keep them so the
        # student also sees negative examples.
        candidates = list(src_images.glob(f"{label_file.stem}.*"))
        if not candidates:
            continue
        img_src = candidates[0]
        shutil.copy2(img_src, dst_images / img_src.name)
        shutil.copy2(label_file, dst_labels / label_file.name)
        n += 1
    return n


def main() -> int:
    p = argparse.ArgumentParser(description=__doc__)
    p.add_argument("--train-src", required=True)
    p.add_argument("--val-src", required=True)
    p.add_argument("--out-dir", required=True)
    args = p.parse_args()

    out_dir = Path(args.out_dir)
    n_train = copy_split(
        Path(args.train_src),
        out_dir / "images" / "train",
        out_dir / "labels" / "train",
    )
    n_val = copy_split(
        Path(args.val_src),
        out_dir / "images" / "val",
        out_dir / "labels" / "val",
    )

    yaml_path = out_dir / "dataset.yaml"
    lines = [
        "# Auto-generated by build_distill_dataset.py.",
        "# 80 COCO class names so the YOLOv8n-Seg COCO-pretrained head",
        "# is preserved (only class 0 = person carries gradient signal",
        "# from SAM 3 pseudo-labels).",
        f"path: {out_dir.resolve()}",
        "train: images/train",
        "val: images/val",
        "names:",
    ]
    for i, name in enumerate(COCO_NAMES):
        lines.append(f"  {i}: {name}")
    yaml_path.write_text("\n".join(lines) + "\n")

    print(f"Train: {n_train} samples -> {out_dir/'images'/'train'}")
    print(f"Val:   {n_val} samples -> {out_dir/'images'/'val'}")
    print(f"Dataset YAML: {yaml_path}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
